# Dados dos números, mostrar la suma, resta, división y multiplicación de ambos

num_1 = int(input('Ingresa un número: '))
num_2 = int(input('Ingresa otro número: '))

suma = num_1 + num_2
resta = num_1 - num_2
multiplicación = num_1 * num_2
división = num_1 / num_2

print('Suma: ', suma)
print('Resta: ', resta)
print('Multiplicación: ', multiplicación)
print('División: ', división)